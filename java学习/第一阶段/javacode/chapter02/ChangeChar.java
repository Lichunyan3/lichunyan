
public class ChangeChar{
	public static void main(String[] args){
		//\t:一个制表位，实现对齐功能
		System.out.println("北京\t上海\t天津");
		//\n:换行符
		System.out.println("jack\nsmith\nmary");
		//\\:一个\
		System.out.println("hello\\milan");
		//\":一个"
		System.out.println("老韩说\"要好好学习\"");
		//\':一个'
		//\r:一个回车
		//输完天气晴朗，光标定在天前面，后面的话重新覆盖
		System.out.println("天气晴朗\r要好好学习");
	}
}