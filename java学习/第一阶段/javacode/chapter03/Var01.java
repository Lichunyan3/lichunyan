public class Var01{
	public static void main(String[] args){
		//声明变量
		int a;
		a = 100;
		System.out.println(a);
		//还可以这样使用
		int b = 800;
		System.out.println(b);
		//记录人的信息
		int age = 20;
		double score = 88.9;
		char gender = '男';
		String name = "Jerry";
		//输出信息
		System.out.println("人的信息如下：");
		System.out.println(name);
		System.out.println(age);
		System.out.println(score);
		System.out.println(gender);
		
	}
}